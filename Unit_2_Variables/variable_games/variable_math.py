#Four main mathematical operations
#Addition, subtraction, division, multiplication
# + , - , *, /
#They can work with variables!!

num1 = 5
num2 = 2

print(num1 + num2) # addition
print(num1 - num2) # subtraction
print(num1 * num2) # multiplication
print(num1 / num2) # division

#Only muliplication works for a string and an integer
#If its addition, you have to type cast the integer into a string

word = "high"

print(word + str(num1)) # Concantenation (We did this!)
print(word * num1) #String multiplication
