#What you will learn:
#   - While Loops
# While Loops - Do something WHILE a condition is TRUE

# while condition is true:
#     do something

#If the condition is false, the while loop will stop

number = 1
while number < 3:
  print(number)
  number = number + 1

#What this is doing

# The while loop will keep looping as long as number is less than 3
# Every time the loop runs, number is increased by one

#First loop:

# number = 1
#1 < 3 → True → loop runs
#prints: 1
#number becomes 2

#Second loop:

#number = 2
#2 < 3 → True → loop runs
#prints: 2
#number becomes 3

#Third loop:

#number = 3
#3 < 3 → False → loop stops
#Program ends
