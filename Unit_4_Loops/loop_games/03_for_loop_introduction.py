# A for loop repeats something a set number of times.

# "i" is just a counter. It changes each time the loop runs.
# On each loop:
#   i = 0
#   i = 1
#   i = 2
#   i = 3
#   i = 4

# "range(5)" means: do it 5 times (0, 1, 2, 3, 4).

for i in range(5):
  print(i)



