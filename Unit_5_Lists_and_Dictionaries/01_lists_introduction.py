# What you will learn
#   - Introduction to lists
#   - Indexing

# Lists use square brackets and commas
subjects = ["math","language arts","history","science"]
# This is called indexing.
# To index, put the list name and square brackets, then the position of the value.
# The first value is always 0.
print(subjects[0]) # prints "math"

print("I am going to say every subject!")

# For every item in the list, print each item.

for i in subjects:
  print(i)
